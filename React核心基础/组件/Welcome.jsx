// 函数式组件声明方式
function App() {
  return (
    <div>
      <h1>Hello, World!</h1>
      <p>Welcome to learning React components!</p>
    </div>
  );
}

// 箭头函数声明组件
const Welcome = () => {
  return (
    <div>
      <h1>Hello, World!</h1>
      <p>Welcome to learning React components!</p>
    </div>
  );
};

export default Welcome;
